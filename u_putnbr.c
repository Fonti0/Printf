#include "printf.h"

void	u_putnbr(int nb)
{
	int	result;
	
	if (nb == -2147483648)
	{
		write (1, "2147483648", 10);
		return;
	}
	if (nb < 0)
		nb = -nb;
	if (nb > 9)
		u_putnbr(nb / 10);
	result = (nb % 10) + '0';
	write (1, &result, 1);
}

